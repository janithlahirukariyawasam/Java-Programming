package com.company;

public class Main {

    public static void main(String[] args) {
        
        int maxValue = Integer.MAX_VALUE;
        int minValue = Integer.MIN_VALUE;
        System.out.println("Max Value "+maxValue+" Min Value "+minValue);

        byte maxByteValue = Byte.MAX_VALUE;
        byte minByteValue = Byte.MIN_VALUE;
        System.out.println("Max Value "+maxByteValue+" Min Value "+minByteValue);

        short maxShortValue = Short.MAX_VALUE;
        short minShortValue = Short.MIN_VALUE;
        System.out.println("Max Value "+maxShortValue+" Min Value "+minShortValue);

        long maxLongValue = Long.MAX_VALUE;
        long minLongValue = Long.MIN_VALUE;
        System.out.println("Max Value "+maxLongValue+" Min Value "+minLongValue);

        long value = 100L; //You need to put 'L' after long value.
        System.out.println(value);

        
    }
}
